<h1 style="max-width:100pt; margin: auto; margin-top:0px;padding-top: 50px;">
    Cégünkről
</h2>
<p style="max-width:60%; margin: auto; margin-top:50px; padding-bottom:150px;">Ez a weboldal az országunk legnagyobb szélerőmű adatbázisa. 
Itt megtalálhatja az összes működésben lévő szélerőművet.
    Megtekintheti, hogy melyik megyében és melyik helységben hány darab szélerőmű működik jelenleg. 
    Emellett oldalunk lehetővé tesz néhány plusz funkciót is az Ön számára.
    Megtekintheti a különböző pénznemek átváltási értékét egy adott napon, valamint egy megadott intervallumban, továbbá egy kiválasztott devizapár 
    átváltási értékét is lekérheti, amelyet az oldalunk egy táblázatban jelenít meg és egy grafikonon is ábrázolja az értékek változását.
    Az oldalon emellett megtalálható egy híroldal, ahol a legfrissebb újdonságokat olvashatja a szélerőművekkel kapcsolatban, továbbá hozzászólásokat fűzhet hozzájuk.
    Ez a funkció viszont regisztációhoz kötött, melyet a belépés menüpont alatt megtehet. 
    Amennyiben már korábban regisztált az oldalunkra, akkor a belépés után válik elérhetővé Ön számára az említett funkció.</p>
